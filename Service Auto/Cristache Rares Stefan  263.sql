CREATE TABLE CLIENT (
  CNP_CLIENT NUMBER(15) NOT NULL PRIMARY KEY,
  NUME VARCHAR2(50) NOT NULL,
  PRENUME VARCHAR2(50) NOT NULL,
  ADRESA VARCHAR2(255),
  TELEFON VARCHAR2(20),
  EMAIL VARCHAR2(50)
);

CREATE TABLE MASINA (
  SERIE_SASIU VARCHAR2(50) NOT NULL PRIMARY KEY,
  MARCA VARCHAR2(50) NOT NULL,
  MODEL VARCHAR2(50) NOT NULL,
  AN_FABRICATIE NUMBER(4) NOT NULL,
  NR_INMATRICULARE VARCHAR2(50),
  CNP_CLIENT NUMBER(15) NOT NULL 
);

CREATE TABLE PROGRAMARI (
  NR_PROGRAMARE NUMBER(10) NOT NULL PRIMARY KEY,
  DATA_PROGRAMARE DATE NOT NULL,
  SERIE_SASIU VARCHAR2(50),
  DESCRIERE VARCHAR2(255) 
);

CREATE TABLE SERVICII (
  ID_SERVICIU NUMBER(10) NOT NULL PRIMARY KEY,
  DENUMIRE_SERVICIU VARCHAR2(50) NOT NULL,
  PRET DECIMAL(10,2) NOT NULL,
  DESCRIERE VARCHAR2(255)
);

CREATE TABLE PIESE (
  ID_PIESA NUMBER(10) NOT NULL PRIMARY KEY,
  DENUMIRE_PIESA VARCHAR2(50) NOT NULL,
  PRET DECIMAL(10,2) NOT NULL,
  CANTITATE NUMBER(10)
);

CREATE TABLE ANGAJAT (
  CNP_ANGAJAT NUMBER(15) NOT NULL PRIMARY KEY,
  NUME VARCHAR2(50) NOT NULL,
  PRENUME VARCHAR2(50) NOT NULL,
  ADRESA VARCHAR2(255),
  TELEFON VARCHAR2(20),
  EMAIL VARCHAR2(50),
  SALARIU DECIMAL(10,2) NOT NULL,
  DATA_ANGAJARE DATE NOT NULL
);

CREATE TABLE FACTURA (
  ID_FACTURA NUMBER(10) NOT NULL PRIMARY KEY,
  DATA_EMITERE DATE NOT NULL,
  TOTAL_PLATA DECIMAL(10,2) NOT NULL
);


CREATE TABLE NECESITA (
  ID_NECESITA NUMBER(10) NOT NULL PRIMARY KEY,
  NR_PROGRAMARE NUMBER(10) NOT NULL ,
  ID_SERVICIU NUMBER(10) NOT NULL
);

CREATE TABLE IMPUN (
  ID_IMPUN NUMBER(10) NOT NULL PRIMARY KEY,
  ID_SERVICIU NUMBER(10) NOT NULL,
  ID_PIESA NUMBER(10) 
